    public static class BlockIDExtensions
    {
        /// <summary>
        /// Transfer items to their equivalent block id
        /// </summary>
        public static BlockID ToBlock(this BlockID id)
        {
            switch (id)
            {
                case BlockID.FlintandSteel:
                    return BlockID.Fire;
                case BlockID.SugarCane:
                    return BlockID.SugarCaneBlock;

                case BlockID.Waterbucket:
                    return BlockID.Water;
                case BlockID.Lavabucket:
                    return BlockID.Lava;
                case BlockID.FlowerPot:
                    return BlockID.FlowerPotBlock;
                case BlockID.Sign:
                    return BlockID.SignPostI;
                case BlockID.ItemFrame:
                    return BlockID.WallSign;
                case BlockID.Skull:
                    return BlockID.SkullBlock;
                case BlockID.String:
                    return BlockID.Tripwire;
                case BlockID.Seeds:
                    return BlockID.SeedsBlock;
                case BlockID.WoodenDoor:
                    return BlockID.WoodenDoorBlock;
                case BlockID.IronDoor:
                    return BlockID.IronDoorBlock;
                case BlockID.Redstone:
                    return BlockID.RedstoneWire;
                case BlockID.Bed:
                    return BlockID.BedBlock;
                case BlockID.RedstoneRepeater:
                    return BlockID.RedstoneRepeaterOff;
                case BlockID.PumpkinSeed:
                    return BlockID.PumpkinPlant;
                case BlockID.MelonSeed:
                    return BlockID.MelonPlant;
                case BlockID.NetherWart:
                    return BlockID.NetherWartBlock;
                case BlockID.Cauldron:
                    return BlockID.CauldronBlock;
                case BlockID.BrewingStand:
                    return BlockID.BrewingStandBlock;
                case BlockID.Carrot:
                    return BlockID.CarrotsBlock;
                case BlockID.Potato:
                    return BlockID.PotatoBlock;
                case BlockID.Cake:
                    return BlockID.CakeBlock;
            }
            //Disabled to allow dye pass through
            if ((int)id > 255)
                return 0;
            return id;

        }
    }

